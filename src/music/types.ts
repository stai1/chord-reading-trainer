export type Accidental = 'flat' | null | 'sharp';

/** Stored-on-Note accidental: covers double accidentals needed for note rendering. */
export type DrawnAccidental =
  | 'double-flat'
  | 'flat'
  | null
  | 'natural'
  | 'sharp'
  | 'double-sharp';

export type AccidentalSymbol = '__' | '_' | '' | '=' | '^' | '^^';

export type Quality =
  | 'major'
  | 'minor'
  | 'diminished'
  | 'augmented'
  | 'major-7th'
  | 'minor-7th'
  | 'dominant-7th'
  | 'diminished-7th'
  | 'half-diminished-7th'
  | 'minor-major-7th'
  | 'augmented-major-7th';

export type Clef = 'treble' | 'bass' | 'both';

export type ExerciseType = 'staff' | 'leadsheet';

export type FigureType = 'note' | 'interval' | 'triad' | '7th';

export type NumeralSystem = 'scale-relative' | 'major-referential';

export type DisplayMode = 'major' | 'minor';

/**
 * The 15 supported key signatures. Each entry stores the number of sharps
 * (positive) or flats (negative) and the letter names for both major and
 * relative-minor display.
 */
export interface KeySignature {
  id: string;
  fifths: number;
  majorTonic: string;
  minorTonic: string;
}

