/**
 * A chord pool entry: stored as scale degree (1-7) and quality, relative to
 * the major mode of the active key signature.
 */
export interface ChordPoolEntry {
  root: number;
  accidental: Accidental;
  quality: Quality;
}

/**
 * Letter + accidental + octave, where octave starts at 0 for the root note.
 * E.g. { letter: 'E', accidental: null, octave: 0 } => "E0".
 *
 * The accidental field stores the *drawn* accidental, which may be a double
 * accidental (notes only, not normally generated by chord construction).
 * 'natural' means an explicit natural sign must be drawn (because the key
 * signature would otherwise alter this letter).
 */
export interface Note {
  letter: string;
  accidental: DrawnAccidental;
  octave: number;
}

/**
 * A single drawn exercise's full payload.
 */
export interface Exercise {
  exerciseType: ExerciseType;
  keySignature: KeySignature;
  figureType: FigureType;
  /** Major or minor display mode for lead sheet rendering. */
  displayMode: DisplayMode;
  /** For lead sheet: the chord pool entry. For staff: the source chord/note. */
  poolEntry: ChordPoolEntry;
  /** Inversion index 0..N-1 applied (0 = root position). */
  inversion: number;
  /** Resolved notes after inversion and octave placement. */
  notes: Note[];
  /** For both-clef placement, the notes split across each clef. */
  trebleNotes?: Note[];
  bassNotes?: Note[];
  /** For staff: the chosen clef placement. */
  clef?: Clef;
  /** Unique key for dedupe within a set (figure type + root + inv + clef). */
  shuffleKey: string;
}
